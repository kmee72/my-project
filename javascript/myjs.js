// 한줄 주석
// 함수 선언(선언과 호출은 다름)
// function: 함수임을 나타내는 예약어(파랑색 글씨, 바꿀수 없음, alert도 예약어) 
// myFunc: 함수이름(사용자 지정, 개인이 명명한 이름), 
// () : 소괄호는 함수
// {} : 중괄호(scope, Block) - 코드의 시작과 끝을 알려줌
function myFunc(){
  alert("함수 호출됨");
}

//  함수 선언과 호출은 다르다!
// 선언은 정의하는 것이고, 호출은 실제 실행되는 것이다.(선언이 되었다고 해서 실행되는 것 아님)